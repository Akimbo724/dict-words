words = {
    "кринж": "стыд",
    "рофл": "прикол",
    "мем": "шутка",
    "базара джекса": "без проблем",
    "пон": "понятно",
    "поч": "почему"
}
word = input("Какое слово вы хотите перевести ?").lower()
if word in words:
    print("Ваш перевод:", words[word])
else:
    print("такого слова нет в словаре")
